package ex1;

/**
 * Class utilitaire calculer.
 */
public class Calcul {

	/**
	 * Add int.
	 *
	 * @param a the a
	 * @param b the b
	 * @return the int
	 */
	public int add(int a, int b){
		return a+b;
	}

	/**
	 * Subtract int.
	 *
	 * @param a the a
	 * @param b the b
	 * @return the int
	 */
	public int subtract(int a, int b){
		return a-b;
	}
}
